package parcial6;

public class Postulante {
    private String nombre;
    private int notaTesis;
    private int anios;

    public Postulante(String nombre, int notaTesis, int anios) {
        this.nombre = nombre;
        this.notaTesis = notaTesis;
        this.anios = anios;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getNotaTesis() {
        return notaTesis;
    }

    public void setNotaTesis(int notaTesis) {
        this.notaTesis = notaTesis;
    }

    public int getAnios() {
        return anios;
    }

    public void setAnios(int anios) {
        this.anios = anios;
    }
    
    
    
    
    
    
}
